// Get current language from localStorage or default to English
function getCurrentLanguage() {
    return localStorage.getItem('language') || 'en';
}

// Set language and persist across all pages
function setLanguage(lang) {
    localStorage.setItem('language', lang);
    document.documentElement.lang = lang;
    
    // Set RTL for Arabic
    if (lang === 'ar') {
        document.documentElement.dir = 'rtl';
        document.body.style.direction = 'rtl';
    } else {
        document.documentElement.dir = 'ltr';
        document.body.style.direction = 'ltr';
    }
    
    translatePage();
    updateLanguageSelector(lang);
}

// Update language selector to show current language
function updateLanguageSelector(lang) {
    const selector = document.getElementById('languageSelector');
    if (selector) {
        selector.value = lang;
    }
}

// Translate the page
function translatePage() {
    const lang = getCurrentLanguage();
    const trans = translations[lang];
    
    // Translate all elements with data-translate attribute
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        if (trans[key]) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = trans[key];
            } else {
                element.textContent = trans[key];
            }
        }
    });
    
    // Translate placeholders
    document.querySelectorAll('[data-translate-placeholder]').forEach(element => {
        const key = element.getAttribute('data-translate-placeholder');
        if (trans[key]) {
            element.placeholder = trans[key];
        }
    });
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', () => {
    const currentLang = getCurrentLanguage();
    setLanguage(currentLang);
});

// Language selector change event
function changeLanguage(lang) {
    setLanguage(lang);
    
    // Update chatbot if it exists
    if (window.floatingChatbot && window.floatingChatbot.isOpen) {
        window.floatingChatbot.updateWelcomeMessage();
    }
}

// Export for use in other scripts
if (typeof module !== 'undefined' && module.exports) {
    module.exports = { setLanguage, getCurrentLanguage, translations };
}